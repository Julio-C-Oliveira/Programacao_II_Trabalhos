package turnSection;


public class TurnAction {
    private String friendAction;
    private String foeAction;
    private String isDead;
    
    public String getFriendAction() {
        return friendAction;
    }
    public void setFriendAction(String friendAction) {
        this.friendAction = friendAction;
    }
    public String getFoeAction() {
        return foeAction;
    }
    public void setFoeAction(String foeAction) {
        this.foeAction = foeAction;
    }
    public String getIsDead() {
        return isDead;
    }
    public void setIsDead(String isDead) {
        this.isDead = isDead;
    }
    
}
